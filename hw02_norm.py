z1_list1 = [2, -5, 8, 9, -25, 25, 4]
z1_list2 = []

for z1_unit1 in z1_list1:
    if z1_unit1 > 0 and math.sqrt(z1_unit1).is_integer():
        z1_list2.append(int(math.sqrt(z1_unit1)))
print(z1_list2)

